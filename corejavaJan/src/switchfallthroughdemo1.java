
public class switchfallthroughdemo1 {

	public static void main(String[] args) {

		switch(4) {
		
		case 1:
		case 2:
		case 3:
		case 4:
			System.out.println("summer");
			break;
		case 5: 
		case 6:
		case 7:
			System.out.println("rainy");
			break;
		case 8:
		case 9:
		case 10:
			System.out.println("winter");
			break;
		default:
		System.out.println("put wrong enter value");
		}
	}

}
